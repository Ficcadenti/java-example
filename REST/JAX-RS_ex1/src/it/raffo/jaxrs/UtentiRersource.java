	private static final Logger logger = Logger.getLogger(UtentiRersource.class);
	public UtentiRersource() {
		String log4jConfPath = "/resources/log4j-client.properties";
		PropertyConfigurator.configure(UtentiRersource.class.getResourceAsStream(log4jConfPath));
	}
	@GET
	@Path("{userId}")
    @Produces(MediaType.APPLICATION_XML)
	public Utente getXml(@PathParam("userId") int userId)
	{
		logger.debug("userId="+userId);
		Utente u=new Utente();
		u.setEmail("raffaele.ficcadenti@gmail.com");
		u.setName("Raffaele Ficcadenti");
		return u;
	}
	
	@GET
	@Path("/test_get")
    @Produces(MediaType.TEXT_PLAIN)
	public String getSomething(@QueryParam("request") String request ,
			 @DefaultValue("1") @QueryParam("version") int version) {

		if (logger.isDebugEnabled()) {
			logger.debug("Start getSomething");
			logger.debug("data: '" + request + "'");
			logger.debug("version: '" + version + "'");
		}

		String response = null;

        try{			
            switch(version){
	            case 1:
	                if(logger.isDebugEnabled()) logger.debug("in version 1");

	                response = "Response from Jersey Restful Webservice : " + request;
                    break;
                default: throw new Exception("Unsupported version: " + version);
            }
        }
        catch(Exception e){
        	response = e.getMessage().toString();
        }
        
        if(logger.isDebugEnabled()){
            logger.debug("result: '"+response+"'");
            logger.debug("End getSomething");
        }
        return response;	
	}
	
